public enum Estados {
	/**
	 * Estado que indica que una propiedad se encuentra a la venta
	 */
	VENTA,
	/**
	 * Estado que indica que una propiedad ya fue vendida
	 */
	VENDIDA,
	/**
	 * Estado que indica que una propiedad se encuentra hipotecada
	 */
	HIPOTECADA
}